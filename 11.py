#loat can also be scientific numbers with an "e" to indicate the power of 10.#

x = 35e3
y = 12E4
z = -87.7e100

print(type(x))
print(type(y))
print(type(z))



#Complex numbers are written with a "j" as the imaginary part#



y = 5j
z = -5j

print(type(x))
print(type(y))
print(type(z))







